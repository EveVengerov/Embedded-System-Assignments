#ORG 6000H
	INITIALIZE:
	LXI H,5000H
	MOV B,M
	MOV C,B
	INX H

	START:
	MOV A,M
	PUSH H
	INX H
	DCR B
	JZ STOP
	MOV C,B
	JZ STOP
	CALL MIN
	POP H
	CMP M
	JC SWAP
	JMP START

	SWAP:
	MOV A,M
	XCHG 
	MOV C,M
	MOV M,A
	XCHG
	MOV M,C
	INX H 
	JMP START

	MIN:
	CMP M
	JC CONTINUE
	PUSH H
	POP D
	MOV A,M
	INX H
	DCR C
	RZ
	JMP MIN
	CONTINUE:
	INX H
	DCR C
	RZ
	JMP MIN

	 STOP:
	HLT
#ORG 5000H  
#DB 05H,11H,15H,14H,10H,04H
	